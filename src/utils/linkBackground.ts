export default function linkBackground(name: string) {
  switch (name) {
    case "github":
      return "#1A1A1A";
    case "frontendmentor":
      return "#FFF";
    case "twitter":
      return "#43B7E9";
    case "linkedin":
      return "#2D68FF";
    case "youtube":
      return "#EE3939";
    case "facebook":
      return "#2442AC";
    case "twitch":
      return "#EE3FC8";
    case "devto":
      return "#333";
    case "codewars":
      return "#8A1A50";
    case "freecodecamp":
      return "#8A1A50";
    case "gitlab":
      return "#EB4925";
    case "hashnode":
      return "#0330D1";
    case "stackoverflow":
      return "#EC7100";
  }
}
